package com.model;

public class Offer {
	
	public String coupon_id ="";
	public String vendor_id="";
	public String short_desc="";
	public String vendor_name="";
	public String expire_date="";
	public String remaining_redem="";
	public String vendorlogo="";
	public String getCoupon_id() {
		return coupon_id;
	}
	public void setCoupon_id(String coupon_id) {
		this.coupon_id = coupon_id;
	}
	public String getVendor_id() {
		return vendor_id;
	}
	public void setVendor_id(String vendor_id) {
		this.vendor_id = vendor_id;
	}
	public String getShort_desc() {
		return short_desc;
	}
	public void setShort_desc(String short_desc) {
		this.short_desc = short_desc;
	}
	public String getVendor_name() {
		return vendor_name;
	}
	public void setVendor_name(String vendor_name) {
		this.vendor_name = vendor_name;
	}
	public String getExpire_date() {
		return expire_date;
	}
	public void setExpire_date(String expire_date) {
		this.expire_date = expire_date;
	}
	public String getRemaining_redem() {
		return remaining_redem;
	}
	public void setRemaining_redem(String remaining_redem) {
		this.remaining_redem = remaining_redem;
	}
	public String getVendorlogo() {
		return vendorlogo;
	}
	public void setVendorlogo(String vendorlogo) {
		this.vendorlogo = vendorlogo;
	}

}
