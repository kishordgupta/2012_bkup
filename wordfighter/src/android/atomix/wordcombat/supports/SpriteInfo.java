package android.atomix.wordcombat.supports;

public class SpriteInfo {
	
	private int spritePositionX;
	private int spritePositionY;
	
	private int spriteInterceptX;
	private int spriteInterceptY;
	
	public int getSpritePositionX() 
	{
		return spritePositionX;
	}
	public void setSpritePositionX(int spritePositionX) 
	{
		this.spritePositionX = spritePositionX;
	}
	public int getSpritePositionY() 
	{
		return spritePositionY;
	}
	public void setSpritePositionY(int spritePositionY) 
	{
		this.spritePositionY = spritePositionY;
	}
	public int getSpriteInterceptX() 
	{
		return spriteInterceptX;
	}
	public void setSpriteInterceptX(int spriteInterceptX) 
	{
		this.spriteInterceptX = spriteInterceptX;
	}
	public int getSpriteInterceptY() 
	{
		return spriteInterceptY;
	}
	public void setSpriteInterceptY(int spriteInterceptY) 
	{
		this.spriteInterceptY = spriteInterceptY;
	}
}
